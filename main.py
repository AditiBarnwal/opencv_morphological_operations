# Python OpenCV Morphological operations are one of the Image processing techniques
# that processes image based on shape. This processing strategy is usually performed on - Binary Images.

# Morphological operations based on OpenCV:

# Erosion
# Dilation
# Opening
# Closing
# Morphological Gradient
# Top hat
# Black hat

# For above techniques the 2 important requirements are :
#         -   the binary image and
#         -  a kernel structuring element that is used to slide across the image.
